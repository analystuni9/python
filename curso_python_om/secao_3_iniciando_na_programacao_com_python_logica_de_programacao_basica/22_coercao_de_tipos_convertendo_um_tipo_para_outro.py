# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1')))  # Converte uma string em um inteiro e imprime o valor e o tipo resultantes
print(type(float('1') + 1))  # Converte uma string em um float, realiza a operação e imprime o tipo do resultado
print(bool(' '))  # Converte uma string em um booleano e imprime o resultado da conversão
print(str(11) + 'b')  # Converte um inteiro em uma string e concatena com outra string, resultando em uma nova string
