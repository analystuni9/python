# Atribuição de valores às variáveis
nome = 'Danilo Brito'  # Define o nome como 'Danilo Brito'
altura = 1.80  # Define a altura como 1.80
peso = 95  # Define o peso como 95

# Cálculo do IMC (Índice de Massa Corporal)
imc = peso / altura ** 2  # Calcula o IMC utilizando a fórmula peso / (altura^2)

# Impressão das informações
print(nome, 'tem', altura, 'de altura,')  # Imprime o nome e a altura
print('pesa', peso, 'quilos e seu imc é')  # Imprime o peso e uma mensagem sobre o IMC
print(imc)  # Imprime o valor do IMC calculado
