
# Em Python, você pode usar três tipos principais de comentários:

# """DocString""": Usado para fornecer documentação detalhada sobre classes, funções e módulos. É uma boa prática incluir docstrings para explicar o propósito e o funcionamento do código.

# '''Comentário em bloco''': Pode ser usado para escrever notas, observações ou explicações longas dentro do código. É útil quando você precisa de mais espaço para escrever comentários.

# Comentário de linha: Usado para adicionar comentários breves em uma linha de código. Geralmente é utilizado para fornecer contexto ou explicação sobre uma linha específica de código.

# No exemplo dado:

# """DocString""": Foi usado para fornecer uma documentação geral do código ou de uma função específica.
